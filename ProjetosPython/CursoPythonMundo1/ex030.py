#030 Crie um programa que leia um número inteiro e mostre na tela se ele é PAR ou ÍMPAR


num = int(input('Arca:Informe um número inteiro qualquer:'))


if num % 2 ==0:
    print('Arca: O número escolhido é PAR')
else:
    print('Arca:O número escolhido é ÍMPAR')

print('\n----------'
      '\nArca:Programa finalizado')

