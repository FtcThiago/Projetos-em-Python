celsius = float(input('Arca:Informe a temperatura em C°:'))
fahrenheit = (celsius * 9)/5 + 32


print('-----------------------------------------------------------------'
      '\nArca: transformando a temperatura em grau celcius para fahrenheit: '
      '\nCalculando....\nanalisando temperatura em fahrenheit....'
      '\n-----------------------------------------------------------------')


print(f'A temperatura de {celsius} grau celsius para fahrenheit é :{fahrenheit}°F')
print('Logo a achamos {1}°F com {0}°C'.format(celsius, fahrenheit))

